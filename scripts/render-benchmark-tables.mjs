#!/usr/bin/env node

// Regenerate the Markdown benchmark tables in README.md and
// docs/content/performance.md from a benchmark JSON report produced by
// `benchmarks/bundle-size/parse-benchmark.mjs --json <path>`.
//
// The tables are kept between marker comments so this script can replace the
// block in place without disturbing the surrounding prose:
//
//   <!-- benchmark:tables:start -->
//   ...generated tables...
//   <!-- benchmark:tables:end -->
//
// Usage:
//   node benchmarks/bundle-size/parse-benchmark.mjs --runs 7 --json /tmp/b.json
//   node scripts/render-benchmark-tables.mjs /tmp/b.json
//
// The numbers are host-dependent; the relative ordering between engines is the
// stable part. Run it on a consistent machine (or the benchmark CI runner) so
// the committed numbers stay comparable.

import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");

const START_MARKER = "<!-- benchmark:tables:start -->";
const END_MARKER = "<!-- benchmark:tables:end -->";

// Which sizes to render, and the suites within each. `large` and `huge` mirror
// the historical README layout (48.7 KB and ~1 MB).
const SIZES = ["large", "huge"];
const SUITES = [
  { key: "parseOnly", title: "Parse Only" },
  { key: "parseAndRender", title: "Parse + Render" },
];

const TARGETS = [join(ROOT, "README.md"), join(ROOT, "docs", "content", "performance.md")];

function parseArgs(argv) {
  const opts = { jsonPath: "/tmp/ox-bench-result.json" };
  for (const arg of argv) {
    if (arg.startsWith("--")) {
      throw new Error(`Unknown argument: ${arg}`);
    }
    opts.jsonPath = arg;
  }
  return opts;
}

function sizeHeading(sizeName, bytes) {
  const kb = bytes / 1024;
  return kb >= 1024 ? `~${Math.round(kb / 1024)} MB` : `${kb.toFixed(1)} KB`;
}

function formatOps(ops) {
  return Math.round(ops).toLocaleString("en-US");
}

function formatAvgMs(avgMs) {
  // Match the README precision: 2 decimals, but keep more for sub-0.01 ms.
  const decimals = avgMs < 0.01 ? 4 : 2;
  return `${avgMs.toFixed(decimals)} ms`;
}

function formatThroughput(mbs) {
  return `${mbs.toFixed(2)} MB/s`;
}

// Render one Markdown table for a (size, suite). Rows are sorted by ops/sec
// descending, matching how the harness prints them. Errored rows are skipped
// so a missing optional library never leaves an "Error" line in the docs.
//
// Columns are width-aligned to match the repo's Markdown formatter output, so
// the generated tables are a formatter fixed point: regenerating never produces
// a diff that `vp fmt` would then re-touch.
function renderTable(rows) {
  const valid = rows.filter((r) => !r.error && r.opsPerSec > 0);
  valid.sort((a, b) => b.opsPerSec - a.opsPerSec);

  const headers = ["Library", "ops/sec", "avg time", "throughput"];
  // Library is left-aligned; the numeric columns are right-aligned.
  const alignRight = [false, true, true, true];

  const cells = valid.map((row) => [
    `\`${row.name}\``,
    formatOps(row.opsPerSec),
    formatAvgMs(row.avgMs),
    formatThroughput(row.throughputMBs),
  ]);

  // Column width = widest cell or header, with a floor of 3 so the alignment
  // marker (`:--` / `--:`) always fits.
  const widths = headers.map((header, col) =>
    Math.max(3, header.length, ...cells.map((cell) => cell[col].length)),
  );

  const pad = (text, col) =>
    alignRight[col] ? text.padStart(widths[col]) : text.padEnd(widths[col]);

  const formatRow = (cols) => `| ${cols.map((text, col) => pad(text, col)).join(" | ")} |`;

  const separator = `| ${widths
    .map((width, col) => (alignRight[col] ? `${"-".repeat(width - 1)}:` : "-".repeat(width)))
    .join(" | ")} |`;

  return [formatRow(headers), separator, ...cells.map(formatRow)].join("\n");
}

function buildBlock(report) {
  const generatedAt = report.generatedAt ? report.generatedAt.split("T")[0] : "unknown date";
  const runs = report.runs ?? 1;
  const out = [
    START_MARKER,
    "",
    `_Benchmark sweep generated on ${generatedAt} (median of ${runs} runs). Numbers track the host machine; the relative ordering between engines is the stable signal. Regenerated by \`scripts/render-benchmark-tables.mjs\`._`,
    "",
  ];

  for (const sizeName of SIZES) {
    const size = report.sizes?.[sizeName];
    if (!size) {
      console.warn(`Size "${sizeName}" missing from report; skipping its tables.`);
      continue;
    }
    const heading = sizeHeading(sizeName, size.bytes);
    for (const suite of SUITES) {
      const rows = size.suites?.[suite.key];
      if (!rows) {
        console.warn(`Suite "${suite.key}" missing for size "${sizeName}"; skipping.`);
        continue;
      }
      out.push(`### ${suite.title} (${heading})`, "", renderTable(rows), "");
    }
  }

  out.push(END_MARKER);
  return out.join("\n");
}

function replaceBlock(filePath, block) {
  const content = readFileSync(filePath, "utf8");
  const start = content.indexOf(START_MARKER);
  const end = content.indexOf(END_MARKER);
  if (start === -1 || end === -1 || end < start) {
    throw new Error(
      `Could not find benchmark markers in ${filePath}. Expected ${START_MARKER} … ${END_MARKER}.`,
    );
  }
  const before = content.slice(0, start);
  const after = content.slice(end + END_MARKER.length);
  const updated = `${before}${block}${after}`;
  if (updated === content) {
    console.log(`No change: ${filePath}`);
    return;
  }
  writeFileSync(filePath, updated);
  console.log(`Updated ${filePath}`);
}

function main() {
  const opts = parseArgs(process.argv.slice(2));
  const report = JSON.parse(readFileSync(opts.jsonPath, "utf8"));
  const block = buildBlock(report);
  for (const target of TARGETS) {
    replaceBlock(target, block);
  }
}

main();
